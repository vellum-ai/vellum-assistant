/**
 * Hatch a managed assistant.
 *
 * `mode` maps to the platform's hatch semantics:
 *   - `ensure` (server default when omitted): return an existing managed
 *     assistant when possible, else create one. This is the auto-hatch /
 *     first-run behavior — callers should leave `mode` unset for it.
 *   - `create`: provision an *additional* assistant (when multi-assistant
 *     hatching is enabled). Pass this to actually add a new one rather than
 *     get handed back the existing assistant.
 *
 * `status` distinguishes the outcome: 201 = newly created, 200 = an existing
 * assistant was returned (a `create` that the server deduped/declined).
 */
export async function hatchAssistant(
  input?: HatchInput,
  mode?: "create" | "ensure",
): Promise<HatchResult> {
  const { data, error, response } = await assistantsHatchCreate({
    body: input ?? {},
    query: mode ? { mode } : undefined,
    throwOnError: false,
  });

  assertHasResponse(response, error, "Failed to hatch assistant.");

  if (response.ok) {
    return {
      ok: true,
      status: response.status,
      data: data as Assistant,
    };
  }

  return {
    ok: false,
    status: response.status,
    error: toErrorObject(error, response),
  };
}

export async function getAssistant(
  assistantId?: string,
): Promise<GetAssistantResult> {
  if (isGatewayAuthMode()) {
    const lockfileAssistant = getSelectedAssistant();
    if (lockfileAssistant) {
      return {
        ok: true,
        status: 200,
        data: {
          id: lockfileAssistant.assistantId,
          name: lockfileAssistant.name ?? lockfileAssistant.assistantId,
          status: "active",
          is_local: true,
          created: lockfileAssistant.hatchedAt ?? "",
        } as Assistant,
      };
    }
  }
  if (assistantId) {
    const { data, error, response } = await assistantsRetrieve({
      path: { id: assistantId },
      throwOnError: false,
    });

    assertHasResponse(response, error, "Failed to get assistant.");

    if (response.ok) {
      return {
        ok: true,
        status: response.status,
        data: data as Assistant,
      };
    }

    return {
      ok: false,
      status: response.status,
      error: toErrorObject(error, response),
    };
  }

  // No ID provided — prefer platform-managed assistants, fall back to local.
  // Query platform first to avoid pagination issues with hosting=all.
  const platformResult = await assistantsList({
    query: { hosting: "platform" },
    throwOnError: false,
  });

  assertHasResponse(
    platformResult.response,
    platformResult.error,
    "Failed to get assistant.",
  );

  if (!platformResult.response.ok) {
    return {
      ok: false,
      status: platformResult.response.status,
      error: toErrorObject(platformResult.error, platformResult.response),
    };
  }

  const platformResults = platformResult.data?.results ?? [];
  if (platformResults.length > 0) {
    return {
      ok: true,
      status: platformResult.response.status,
      data: platformResults[0]!,
    };
  }

  // No platform assistant found — try local.
  const localResult = await assistantsList({
    query: { hosting: "local" },
    throwOnError: false,
  });

  assertHasResponse(
    localResult.response,
    localResult.error,
    "Failed to get assistant.",
  );

  if (localResult.response.ok) {
    const results = localResult.data?.results ?? [];
    if (results.length > 0) {
      return {
        ok: true,
        status: localResult.response.status,
        data: results[0]!,
      };
    }
  }

  if (!localResult.response.ok) {
    return {
      ok: false,
      status: localResult.response.status,
      error: toErrorObject(localResult.error, localResult.response),
    };
  }

  return {
    ok: false,
    status: 404,
    error: { detail: "No assistants found" },
  };
}

