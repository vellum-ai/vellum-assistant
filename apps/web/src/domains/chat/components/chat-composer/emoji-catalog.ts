/** Returns emoji entries whose shortcode contains `query` (case-insensitive), capped at `limit`. */
export function searchEmoji(query: string, limit = 8): EmojiEntry[] {
  if (!query) return EMOJI_CATALOG.slice(0, limit);
  const lower = query.toLowerCase();
  return EMOJI_CATALOG.filter((e) => e.shortcode.includes(lower)).slice(0, limit);
}
